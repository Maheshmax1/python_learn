# Tara and Jyoti were given one number each.
# She has to find whether the sum of the two numbers
# is divisible by 5 or not . If yes then print 1 else print 0



tara=int(input("Enter your number"))
jyothi=int(input("Enter your number"))

print(tara,jyothi)

add=(tara+jyothi)
print(add)

if add%5 == 0 :
    print(1)
else:
    print(0)
     




